$(document).ready(function () {
    $("#kiptoo").hide()
    $("#ronaldp").hide()
    $("#muriithip").hide()
    $("#victorp").hide()




    $("#kiptooimage").click(function () {
        $("#kiptoo").toggle()

    })

    $("#kiptoo").click(function () {
        $("#kiptoo").show()


    })

    $("#ronaldimage").click(function () {
        $("#ronaldp").toggle()


    })

    $("#ronald").click(function () {
        $("#ronaldp").show()


    })

    $("#muriithi").click(function () {
        $("#muriithip").toggle()


    })

    $("#muriithi").click(function () {
        $(".muriithip").show()


    })


    $("#victor").click(function () {
        $("#victorp").toggle()


    })

    $("#victor").click(function () {
        $(".victorp").show()


    })




})